#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int a;
	int b;
	int c;

	a = size - 1;
	b = 0;
	while (b < size / 2)
	{
		c = tab[a];
		tab[a] = tab[b];
		tab[b] = c;
		b++;
		a--;
	}
}
